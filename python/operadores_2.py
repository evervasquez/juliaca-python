# declaración de variables
variable1=5
variable2=3
lista_precios = [12, 34]

diccionario_precios = {
	"precio1": 12,
	"precio2": 34
}

# comparación
print(variable1 == variable2) # igualdad
print(variable1 != variable2) # diferente
print(variable1 > variable2) # mayor
print(variable1 < variable2) # menor
print(variable1 >= variable2) # mayor o igual
print(variable1 <= variable2) # menor o igual

print(lista_precios[0] == lista_precios[1])
print(diccionario_precios['precio1'] == diccionario_precios['precio2'])
